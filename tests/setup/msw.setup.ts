import { server } from '../../mocks/server';
import { test } from '@playwright/test';

import {
  resetCartMock
} from '../../mocks/handlers/cartHandlers';

import {
  resetCheckoutMock
} from '../../mocks/handlers/checkoutHandlers';

/*
================================
MSW Setup（Aパターン固定版）
================================
責務：
- Nodeプロセス内でMSW起動
- テスト前Mock状態初期化
- テスト終了後MSW停止
================================
*/

/*
================================
MSW 起動フラグ
================================
MSW多重起動防止
================================
*/
let isServerStarted = false;

/*
================================
MSW 起動
================================
Playwright Worker開始後に起動
================================
*/
test.beforeAll(() => {

  /*
  ----------------------------
  未起動時のみMSW起動
  ----------------------------
  */
  if (!isServerStarted) {

    /*
    ----------------------------
    MSW Server起動
    ----------------------------
    */
    server.listen({

      /*
      ----------------------------
      未定義APIは実通信へ流す
      ----------------------------
      */
      onUnhandledRequest: 'bypass',
    });

    /*
    ----------------------------
    起動済みフラグ更新
    ----------------------------
    */
    isServerStarted = true;
  }
});

/*
================================
テスト前リセット
================================
各テスト独立性保証
================================
*/
test.beforeEach(() => {

  /*
  ----------------------------
  Cart Mock初期化
  ----------------------------
  */
  resetCartMock();

  /*
  ----------------------------
  Checkout Mock初期化
  ----------------------------
  */
  resetCheckoutMock();
});

/*
================================
テスト終了後クローズ
================================
全テスト終了時のみ停止
================================
*/
test.afterAll(() => {

  /*
  ----------------------------
  MSW Server停止
  ----------------------------
  */
  server.close();

  /*
  ----------------------------
  起動フラグ初期化
  ----------------------------
  */
  isServerStarted = false;
});