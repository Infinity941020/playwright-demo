import { http, HttpResponse } from 'msw';

/*
================================
Cart型定義
================================
*/

/*
================================
Cart Item型
================================
Cart保存データ
================================
*/
type CartItem = {
  id: number;
  productId: number;
  quantity: number;
};

/*
================================
Cart Request型
================================
Cart追加API request body
================================
*/
type CartRequestBody = {
  productId?: number;
  quantity?: number;
};

/*
================================
Mock DB
================================
Cart状態保持
================================
*/
let cart: CartItem[] = [];

/*
================================
Mock ID採番
================================
MSW内で一意ID管理
================================
*/
let currentCartId = 1;

/*
================================
Cart Mock リセット
================================
各テスト独立性保証
================================
*/
export const resetCartMock = () => {

  /*
  ================================
  Cart初期化
  ================================
  */
  cart = [];

  /*
  ================================
  ID採番初期化
  ================================
  */
  currentCartId = 1;
};

/*
================================
Cart API URL
================================
MSW intercept対象URL
================================
*/
const CART_API_URL = 'http://localhost/api/cart';

/*
================================
Cart Handlers
================================
MSW Cart API定義
================================
*/
export const cartHandlers = [

  /*
  ================================
  Cart一覧取得
  ================================
  */
  http.get(CART_API_URL, async () => {

    return HttpResponse.json(
      {
        items: cart,
      },
      {
        status: 200,
      }
    );
  }),

  /*
  ================================
  Cart追加
  ================================
  */
  http.post(CART_API_URL, async ({ request }) => {

    /*
    ================================
    Request Body取得
    ================================
    */
    const body = (await request.json()) as CartRequestBody | null;

    /*
    ================================
    payload未指定
    ================================
    */
    if (!body) {

      return HttpResponse.json(
        {
          error: 'invalid payload',
        },
        {
          status: 400,
        }
      );
    }

    /*
    ================================
    productId未指定
    ================================
    */
    if (!body.productId) {

      return HttpResponse.json(
        {
          error: 'invalid payload',
        },
        {
          status: 400,
        }
      );
    }

    /*
    ================================
    Cart Item生成
    ================================
    */
    const item: CartItem = {

      /*
      ================================
      Mock採番ID
      ================================
      */
      id: currentCartId++,

      /*
      ================================
      商品ID
      ================================
      */
      productId: body.productId,

      /*
      ================================
      quantity未指定時は1
      ================================
      */
      quantity: body.quantity ?? 1,
    };

    /*
    ================================
    Mock DB保存
    ================================
    */
    cart.push(item);

    /*
    ================================
    作成成功レスポンス
    ================================
    */
    return HttpResponse.json(
      item,
      {
        status: 201,
      }
    );
  }),

  /*
  ================================
  Cart削除
  ================================
  */
  http.delete(`${CART_API_URL}/:id`, async ({ params }) => {

    /*
    ================================
    path parameter取得
    ================================
    */
    const id = Number(params.id);

    /*
    ================================
    Cart削除
    ================================
    */
    cart = cart.filter((item) => item.id !== id);

    /*
    ================================
    削除成功
    ================================
    */
    return HttpResponse.json(
      {
        success: true,
      },
      {
        status: 200,
      }
    );
  }),
];